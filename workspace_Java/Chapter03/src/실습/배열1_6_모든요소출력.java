// (6) 정수를 10개 저장할 수 있는 배열을 생성하고
// 모든 요소의 값을 출력해보아라.
package 실습;
public class 배열1_6_모든요소출력 {
  public static void main(String[] args) {

    // (시작) 정수 00개 <저장후 배열 생성>문
    int[] arr = new int[10];

    // (마무리) 배열의 '모든 all' <요소값 출력>문 //
    for(int i = 0 ; i < arr.length ; i++){
      System.out.println(i);
    }


  }
}

public class 변수_2 {
  public static void main(String[] args) {
    //1.정수 하나를 저장할 수 있는 변수 money를 선언
    int money;

    //2.변수 money에 숫자 100을 지정
    money = 100;

    //3.변수 money의 값을 출력
    System.out.println("money = " + money);

    money = 200;
    System.out.println("money = " + money);

    money = money - 50;
    System.out.println("money = " + money);








  }
}
